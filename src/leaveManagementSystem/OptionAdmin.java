    private void bt_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SearchActionPerformed

        
        JOptionPane.showMessageDialog(null,"Manager Found");
        ManagerId=txt_ManagerID.getText();
        OptionAdmin_Class optionadmin=new OptionAdmin_Class();
        count=optionadmin.Search_Manager(ManagerId);
       
        if(count==1)
            {
                bt_Remove.setVisible(true);
                bt_Edit.setVisible(true);
            }
        else if(count>1)
            JOptionPane.showMessageDialog(null,"Error");
                
        else
            JOptionPane.showMessageDialog(null,"Not Found");
       
    }//GEN-LAST:event_bt_SearchActionPerformed

