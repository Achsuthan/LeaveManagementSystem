    private void bt_generateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_generateActionPerformed
        // TODO add your handling code here:
        
        
        String ID=txt_generate.getText();
        HashMap parameter = new HashMap();
        parameter.put("EMP_ID",ID);
        
        try
        {
            conn=Connect.ConnectDB();
            String path="C:\\Users\\Anojithan\\Documents\\NetBeansProjects\\LeaveManagementSystem\\IReport\\Leave2.jrxml";
            JasperReport jr = JasperCompileManager.compileReport(path);            
            JasperPrint jp = JasperFillManager.fillReport(jr, parameter, conn);
            JasperViewer.viewReport(jp,false);
        }
        
        catch(JRException ex)
        {
            System.out.println(ex.getMessage());
        }
        
        
        
    }//GEN-LAST:event_bt_generateActionPerformed

